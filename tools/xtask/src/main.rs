//! Xtask - Build and development tools for Things 3 integration

mod commands;
use commands::{
    analyze, generate_code, generate_tests, local_dev_clean, local_dev_health, local_dev_setup,
    perf_test, setup_git_hooks, things_backup, things_db_location, things_validate,
};

use anyhow::Result;
use clap::{Parser, Subcommand};

#[derive(Parser)]
#[command(name = "xtask")]
#[command(about = "Build and development tools for Things 3 integration")]
#[command(version)]
struct Cli {
    #[command(subcommand)]
    command: Commands,
}

#[derive(Subcommand)]
enum Commands {
    /// Generate test suites
    GenerateTests {
        /// Target to generate tests for
        target: String,
    },
    /// Generate code
    GenerateCode {
        /// Code to generate
        code: String,
    },
    /// Local development setup
    LocalDev {
        #[command(subcommand)]
        action: LocalDevAction,
    },
    /// Things-specific operations
    Things {
        #[command(subcommand)]
        action: ThingsAction,
    },
    /// Code analysis
    Analyze,
    /// Performance testing
    PerfTest,
    /// Setup git hooks
    SetupHooks,
}

#[derive(Subcommand)]
enum LocalDevAction {
    /// Set up local development environment
    Setup,
    /// Health check
    Health,
    /// Clean build artifacts
    Clean,
}

#[derive(Subcommand)]
enum ThingsAction {
    /// Validate Things database
    Validate,
    /// Backup Things database
    Backup,
    /// Show database location
    DbLocation,
}

fn main() -> Result<()> {
    let cli = Cli::parse();

    match cli.command {
        Commands::GenerateTests { target } => {
            generate_tests(&target);
        }
        Commands::GenerateCode { code } => {
            generate_code(&code);
        }
        Commands::LocalDev { action } => match action {
            LocalDevAction::Setup => {
                local_dev_setup();
            }
            LocalDevAction::Health => {
                local_dev_health();
            }
            LocalDevAction::Clean => {
                local_dev_clean();
            }
        },
        Commands::Things { action } => match action {
            ThingsAction::Validate => {
                things_validate();
            }
            ThingsAction::Backup => {
                things_backup();
            }
            ThingsAction::DbLocation => {
                things_db_location();
            }
        },
        Commands::Analyze => {
            analyze();
        }
        Commands::PerfTest => {
            perf_test();
        }
        Commands::SetupHooks => {
            setup_git_hooks()?;
        }
    }

    Ok(())
}

